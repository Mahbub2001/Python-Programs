'''
 Write a Python program to calculate midpoints of a line
'''
x1 = float(input("Input the value of the x1 of the endpoint-1 : "))
y1 = float(input("Input the value of the y1 of the endpoint-1 : "))

x2 = float(input("Input the value of the x2 of the endpoint-2 : "))
y2 = float(input("Input the value of the y2 of the endpoint-2 : "))


print(f"The midpoint X {(x1+x2)/2} ")
print(f"The midpoint Y {(y1+y2)/2} ")