'''
Write a Python program to concatenate N strings.

'''

list1 = ["Red", "White","Black"]
colors = " - ".join(list1)
print(f"All Colors : {colors}")


list1 = ("Red","Blue","Black")
colors = " - ".join(list1)
print(f"All colors : {colors}")


list1 = {"Red","Blue","Black"}
colors = " - ".join(list1)
print(f"All colors : {colors}")
